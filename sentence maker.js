// Declare variables using let
let adjective = "mighty";
let noun = "dragon";
let verb = "angry";
let place = "volcano";
let adjective2 = "fiery";
let noun2 = "tacos";

// Create first story using string concatenation
let firstStory = "Once upon a time, there was a(n) " + adjective + " " + noun + 
                 " who loved to eat " + noun2 + ". The " + noun +
                 " lived in a " + place + " and had " + adjective2 + 
                 " nostrils that blew fire when it was " + verb + ".";

// Output the first story
console.log("First story: " + firstStory);

// Reassign new values to variables
adjective = "tiny";
